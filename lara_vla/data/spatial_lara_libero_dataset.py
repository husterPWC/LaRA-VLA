"""
Spatial-LaRA LIBERO Dataset Loader
==================================
Sidecar dataset that loads spatial labels (mask, pose, future) for LaRA-VLA
training. Reads the index.jsonl generated by Phase 3D and lazy-loads episode
NPZ files with an LRU cache.

Returns per sample:
    image_agentview:                [3, H, W]
    image_wrist:                    [3, H, W]
    affordance_mask_agentview:     [1, H, W]
    affordance_mask_wrist:         [1, H, W]
    future_affordance_mask_agentview: [1, H, W]
    future_affordance_mask_wrist:     [1, H, W]
    primary_pose_world:            [7]
    future_primary_pose_world:     [7]
    primary_pose_eef:              [7]
    future_primary_pose_eef:       [7]
    robot_state:                   [R_dim]
    actions:                       [H, 7]
    suite / task_id / ...          metadata
"""

import json
import logging
from collections import OrderedDict
from pathlib import Path

import numpy as np
from torch.utils.data import Dataset

logger = logging.getLogger(__name__)


class SpatialLaRALiberoDataset(Dataset):
    """PyTorch Dataset for Spatial-LaRA LIBERO data.

    Reads index.jsonl (one line per frame) and lazy-loads episode NPZ files
    with an LRU cache to avoid repeated disk I/O.

    Args:
        root:       Root directory containing suite/task_xx/demo_xxxxxx/ structure.
        index_path: Path to index.jsonl file.
        future_k:   Future horizon for action chunk and future labels (default 8).
        cache_size: Max number of episode NPZ files cached in memory.
    """

    def __init__(
        self,
        root: str,
        index_path: str,
        future_k: int = 8,
        cache_size: int = 32,
    ):
        super().__init__()
        self.root = Path(root)
        self.future_k = future_k
        self.cache_size = cache_size

        # Load index
        logger.info(f"Loading index from {index_path} ...")
        self.entries = []
        with open(index_path) as f:
            for line in f:
                self.entries.append(json.loads(line))
        logger.info(f"Loaded {len(self.entries)} entries")

        # LRU cache: episode_path -> npz dict
        self._npz_cache = OrderedDict()

        # First entry to peek shapes
        self._peek_entry = self.entries[0]

    def __len__(self):
        return len(self.entries)

    def _load_episode(self, episode_path: str) -> dict:
        """Load an episode NPZ, using LRU cache."""
        if episode_path in self._npz_cache:
            # Move to end (most recently used)
            self._npz_cache.move_to_end(episode_path)
            return self._npz_cache[episode_path]

        # Evict oldest if cache full
        while len(self._npz_cache) >= self.cache_size:
            evict_path, evict_data = self._npz_cache.popitem(last=False)
            evict_data.close()

        npz_path = self.root / episode_path
        data = np.load(npz_path, allow_pickle=False)
        self._npz_cache[episode_path] = data
        return data

    def __getitem__(self, idx: int):
        entry = self.entries[idx]
        episode_path = entry["episode_path"]

        # Index-driven: spatial_idx, fut_spatial_idx, goal_idx are pre-computed and stored
        spatial_idx = entry.get("hdf5_frame_idx", entry.get("frame_idx", 0))
        cot_frame = entry.get("cot_frame_idx", spatial_idx)
        fut_spatial_idx = entry.get("hdf5_future_idx",
                                     entry.get("future_idx", spatial_idx + self.future_k))
        goal_idx = entry.get("subtask_end_idx", fut_spatial_idx)

        data = self._load_episode(episode_path)
        T_hdf5 = data["rgb_agentview"].shape[0]
        T_cot = entry.get("T_cot", T_hdf5)

        # Safety asserts
        assert 0 <= spatial_idx < T_hdf5, f"hdf5_frame_idx {spatial_idx} out of [0, {T_hdf5})"
        assert 0 <= fut_spatial_idx < T_hdf5, f"hdf5_future_idx {fut_spatial_idx} out of [0, {T_hdf5})"
        assert 0 <= goal_idx < T_hdf5, f"subtask_end_idx {goal_idx} out of [0, {T_hdf5})"
        assert 0 <= cot_frame < T_cot, f"cot_frame_idx {cot_frame} out of [0, {T_cot})"
        assert fut_spatial_idx >= spatial_idx, f"future {fut_spatial_idx} < current {spatial_idx}"
        assert goal_idx >= spatial_idx, f"goal {goal_idx} < current {spatial_idx}"
        if entry.get("alignment_method") == "identity_no_noops_hdf5":
            assert cot_frame == spatial_idx, f"identity: cot {cot_frame} != hdf5 {spatial_idx}"
        H_fut = self.future_k

        # ── Images (current, future, goal) ─
        img_agentview = self._to_chw(data["rgb_agentview"][spatial_idx])
        img_wrist = self._to_chw(data["rgb_wrist"][spatial_idx])
        img_agentview_next = self._to_chw(data["rgb_agentview"][fut_spatial_idx])
        img_agentview_goal = self._to_chw(data["rgb_agentview"][goal_idx])

        # ── Masks (current, future, goal) ─
        mask_agentview = data["affordance_mask_agentview"][spatial_idx : spatial_idx + 1]
        mask_wrist = data["affordance_mask_wrist"][spatial_idx : spatial_idx + 1]
        mask_agentview_fut = data["affordance_mask_agentview"][fut_spatial_idx : fut_spatial_idx + 1]
        mask_wrist_fut = data["affordance_mask_wrist"][fut_spatial_idx : fut_spatial_idx + 1]
        mask_agentview_goal = data["affordance_mask_agentview"][goal_idx : goal_idx + 1]
        mask_wrist_goal = data["affordance_mask_wrist"][goal_idx : goal_idx + 1]

        # ── Poses ─
        pose_world = data["primary_pose_world"][spatial_idx].astype(np.float32)
        pose_world_fut = data["primary_pose_world"][fut_spatial_idx].astype(np.float32)
        pose_eef = data["primary_pose_eef"][spatial_idx].astype(np.float32)
        pose_eef_fut = data["primary_pose_eef"][fut_spatial_idx].astype(np.float32)

        # ── Robot state ─
        robot_state = np.concatenate(
            [
                data["robot0_eef_pos"][spatial_idx],
                data["robot0_eef_quat"][spatial_idx],
                data["robot0_gripper_qpos"][spatial_idx],
                data["robot0_joint_pos"][spatial_idx],
            ]
        ).astype(np.float32)

        # ── Actions (from HDF5, for reference) ─
        actions_all = data["actions"].astype(np.float32)
        # Use spatial frame range
        act_start = spatial_idx
        act_end = min(act_start + H_fut, T_hdf5)
        actions_raw = actions_all[act_start:act_end]
        actual_len = actions_raw.shape[0]
        if actual_len == 0:
            last_action = actions_all[spatial_idx : spatial_idx + 1]
            actions = np.tile(last_action, (H_fut, 1))
        elif actual_len < H_fut:
            pad = np.tile(actions_raw[-1:], (H_fut - actual_len, 1))
            actions = np.concatenate([actions_raw, pad], axis=0)
        else:
            actions = actions_raw[:H_fut]

        return {
            # Current (model input)
            "image": img_agentview,
            "image_wrist": img_wrist,
            "current_affordance_mask_agentview": mask_agentview,
            "current_affordance_mask_wrist": mask_wrist,
            # Future (LaRA-VLA image_next + our future mask supervision)
            "image_next": img_agentview_next,
            "future_affordance_mask_agentview": mask_agentview_fut,
            "future_affordance_mask_wrist": mask_wrist_fut,
            # Goal (subtask completion state, supervision only)
            "goal_affordance_mask_agentview": mask_agentview_goal,
            "goal_affordance_mask_wrist": mask_wrist_goal,
            "goal_image_debug": img_agentview_goal,
            # Legacy pose fields (deprecated, kept for compatibility)
            "primary_pose_world": pose_world,
            "future_primary_pose_world": pose_world_fut,
            "primary_pose_eef": pose_eef,
            "future_primary_pose_eef": pose_eef_fut,
            # Robot state & action
            "robot_state": robot_state,
            "actions": actions,
            # Metadata
            "suite": entry["suite"],
            "task_id": entry["task_id"],
            "demo_id": entry.get("demo_id", -1),
            "frame_idx": cot_frame,
            "hdf5_frame_idx": spatial_idx,
            "future_idx": entry.get("cot_future_idx", cot_frame + H_fut),
            "hdf5_future_idx": fut_spatial_idx,
            "subtask_end_idx": goal_idx,
            "T": entry.get("T_cot", T_hdf5),
            "objects_of_interest": entry.get("objects_of_interest", []),
            "primary_object": entry.get("primary_object", ""),
            "camera_names": entry.get("camera_names", []),
        }

    @staticmethod
    def _to_chw(img: np.ndarray) -> np.ndarray:
        """Convert [H, W, 3] uint8 → [3, H, W] float32 (0-1)."""
        return img.astype(np.float32).transpose(2, 0, 1) / 255.0
